void main(List<String> args) {
// 2. Write a program to make addition, Subtraction, Multiplication and
// Division of Two Numbers

  int a = 10;
  int b = 12;

  int add = a + b;
  print("Addition of $a and $b is $add");

  int sub = a - b;
  print("Substraction of $a and $b is $sub");

  int multi = a * b;
  print("Multiplication of $a and $b is $multi");

  double div = a / b;
  print("Division of $a and $b is $div");
}
